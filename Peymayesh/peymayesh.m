% --- Executes on button press in run.
function run_Callback(hObject, eventdata, handles)
% hObject    handle to run (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global l angle n da x y;
set(handles.print,'enable','on')
x(1,1)=str2double(get(handles.edit8,'string'));
y(1,1)=str2double(get(handles.edit7,'string'));
if get(handles.radiobutton6,'value')
    e2=str2double(get(handles.edit10,'string'));
    n2=str2double(get(handles.edit11,'string'));
    de=e2-x(1);
    dn=n2-y(1);
    v=radtodeg(atan(abs(de/dn)));
    alpha=str2double(get(handles.edit16,'string'));
    if de<0
        if dn<0
            g(1)=180+v+alpha;
        else
            g(1)=360-v+alpha;
        end
    else
        if dn<0
            g(1)=180-v+alpha;
        else
            g(1)=v+alpha;
        end
    end
    
elseif get(handles.radiobutton5,'value')
    ddgg=str2double(get(handles.edit15,'string'));
    mmnn=str2double(get(handles.edit14,'string'));
    sscc=str2double(get(handles.edit6,'string'));
    g(1)=ddgg+(mmnn+sscc/60)/60;
end;
for i=1:n-1
    g(i+1)=g(i)+180-angle(i+1);
end

for i=1:n
    x(i+1,1)=x(i,1)+l(i)*sin(degtorad(g(i)));
    y(i+1,1)=y(i,1)+l(i)*cos(degtorad(g(i)));
end
x;
y;
fx=x(n+1,1)-x(1,1);
fy=y(n+1,1)-y(1,1);
f=sqrt((fx^2)+(fy^2));
set(handles.edit13,'string',num2str(f))
fmax=2.5*sum(l)*degtorad(da/3600)*sqrt(n)/4;
set(handles.edit12,'string',num2str(fmax))
for i=1:n
    cx(i)=-l(i)*fx/sum(l);
    cy(i)=-l(i)*fy/sum(l);
end
x(1,2)=x(1,1);
y(1,2)=y(1,1);
for i=1:n
    x(i+1,2)=x(i,2)+l(i)*sin(degtorad(g(i)))+cx(i);
    y(i+1,2)=y(i,2)+l(i)*cos(degtorad(g(i)))+cy(i);
end

