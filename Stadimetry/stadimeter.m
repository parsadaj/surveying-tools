% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global n zt hz tb tp tv x y x1 y1 z
x1=str2double(get(handles.edit2,'string'));
y1=str2double(get(handles.edit1,'string'));
if get(handles.radiobutton2,'value')
    e2=str2double(get(handles.edit10,'string'));
    n2=str2double(get(handles.edit9,'string'));
    de=e2-x1;
    dn=n2-y1;
    v=radtodeg(atan(abs(de/dn)));
    if de<0
        if dn<0
            gor=180+v;
        else
            gor=360-v;
        end
    else
        if dn<0
            gor=180-v;
        else
            gor=v;
        end
    end
    
elseif get(handles.radiobutton1,'value')
    ddgg=str2double(get(handles.edit11,'string'));
    mmnn=str2double(get(handles.edit12,'string'));
    sscc=str2double(get(handles.edit14,'string'));
    gor=ddgg+(mmnn+sscc/60)/60;
end
for i=1:n;
    shib(i)=90-zt(i);
end
k=str2double(get(handles.edit17,'string'));
hi = str2double(get(handles.edit18,'string'));
l=abs(tb-tp);
for i=1:n
    x(i)=sind(gor+hz(i))*k*l(i)*(cosd(shib(i))^2)/1000;
    y(i)=cosd(gor+hz(i))*k*l(i)*(cosd(shib(i))^2)/1000;
    z(i)=k*l(i)*(cosd(shib(i))*sin(shib(i)))/1000;
end

